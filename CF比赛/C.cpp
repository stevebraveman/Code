using namespace STman;
template <typename T>
T lowbit(T x) {
	return x & -x;
}
ll n, p, __, t, m;
int main() {
	read(n), read(p);
	do {
		__ += 1;
		n -= p;
		if (n <= 0) {
			printf("-1");
			return 0;
		}
		m = n;
		t = 0;
		for (int i = m; i; i -= lowbit(i)) {
			t++;
		}
	} while (t > __ || __ > n);
	write(__), et();
	return 0;
}